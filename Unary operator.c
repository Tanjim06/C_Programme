//Create a programme with unary operators.Unary operators are increament operators(++x/x++) and decrement operator(--x/x--)//
#include<stdio.h>
int main()
{
    int x=10;  //variable declaration

    int y=x++;  //variable declaration with post increament operator(x++)

    printf("x=%d\n",x);

    printf("y=%d\n",y);

    return 0;
}
